"""Generate 9x9 Go opening guide with properly formatted diagrams for first 20 moves."""

import os

# Column mapping
COL_MAP = {'A':0, 'B':1, 'C':2, 'D':3, 'E':4, 'F':5, 'G':6, 'H':7, 'J':8}
COL_NAMES = ['A', 'B', 'C', 'D', 'E', 'F', 'G', 'H', 'J']

def parse_coord(coord):
    """Parse coordinate like 'E5' to (col, row) indices."""
    col = COL_MAP[coord[0].upper()]
    row = 9 - int(coord[1])  # row 9 = index 0, row 1 = index 8
    return col, row

def render_diagram(moves, start=1, end=None, prev_stones=None):
    """
    Render a board diagram.
    moves: list of coordinate strings, index 0 = move 1
    start/end: which moves to number (1-indexed)
    prev_stones: if set, stones from moves before 'start' shown as X/O
    """
    if end is None:
        end = len(moves)

    # Initialize empty grid (each cell is 4 chars: " .  ")
    grid = [[' .  ' for _ in range(9)] for _ in range(9)]

    # Place previously numbered stones as X (black) or O (white)
    if prev_stones:
        for i in range(0, min(start - 1, len(moves))):
            coord = moves[i]
            if coord == 'pass':
                continue
            col, row = parse_coord(coord)
            if (i + 1) % 2 != 0:  # Black (odd move number)
                grid[row][col] = ' X  '
            else:  # White (even move number)
                grid[row][col] = ' O  '

    # Place numbered stones for the range [start, end]
    for i in range(start - 1, min(end, len(moves))):
        coord = moves[i]
        if coord == 'pass':
            continue
        col, row = parse_coord(coord)
        num = i + 1
        if num % 2 != 0:  # Black
            if num < 10:
                grid[row][col] = f'({num}) '
            else:
                grid[row][col] = f'({num})'
        else:  # White
            if num < 10:
                grid[row][col] = f'[{num}] '
            else:
                grid[row][col] = f'[{num}]'

    lines = []
    # Match reformat.py format exactly
    lines.append('    A   B   C   D   E   F   G   H   J   ')
    for i in range(9):
        row_num = 9 - i
        cells = ''.join(grid[i])
        lines.append(f' {row_num}  {cells} {row_num}  ')
    lines.append('    A   B   C   D   E   F   G   H   J   ')

    return '\n'.join(lines)


def format_branch(title, intro, moves, move_descs, analysis, midgame_analysis=""):
    """Format a complete branch with two diagrams (1-10 and 11-20)."""
    lines = []
    lines.append(f'### {title}')
    lines.append(f'{intro}')
    lines.append('')

    # Diagram 1: Moves 1-10
    lines.append('**圖一：第 1-10 手**')
    lines.append('')
    lines.append('```')
    lines.append(render_diagram(moves, 1, 10))
    lines.append('```')
    lines.append('')

    # Move descriptions 1-10
    lines.append('**著手解說：**')
    for i in range(min(10, len(move_descs))):
        num = i + 1
        color = '黑' if num % 2 != 0 else '白'
        coord = moves[i].upper()
        lines.append(f'{num}. {color} {coord}：{move_descs[i]}')
    lines.append('')

    if midgame_analysis:
        lines.append(f'**前十手小結**：{midgame_analysis}')
        lines.append('')

    # Diagram 2: Moves 11-20
    if len(moves) > 10:
        lines.append('**圖二：第 11-20 手**（X = 已有黑子，O = 已有白子）')
        lines.append('')
        lines.append('```')
        lines.append(render_diagram(moves, 11, 20, prev_stones=True))
        lines.append('```')
        lines.append('')

        # Move descriptions 11-20
        lines.append('**著手解說（續）：**')
        for i in range(10, min(20, len(move_descs))):
            num = i + 1
            color = '黑' if num % 2 != 0 else '白'
            coord = moves[i].upper()
            lines.append(f'{num}. {color} {coord}：{move_descs[i]}')
        lines.append('')

    lines.append(f'**分析**：{analysis}')
    lines.append('')
    return '\n'.join(lines)


